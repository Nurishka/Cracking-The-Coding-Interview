# implementation of a queue
class Queue(object):
    def __init__(self, data=None): # data is an array
        self.data = data

    def enqueue(data):
        self.data.append(data)

    def dequeue(data):
        self.data.pop[0]

    def getFront():
        return self.data[0]

    def getBack():
        return self.data[-1]
